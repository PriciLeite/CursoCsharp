namespace CursoCsharp.aula_06_Comportamento_de_Memoria___Arrays___Listas.Referencia___Valor
{
    struct Point
    {
        // Criando as Variáveis que estão na Stack por ser uma Struct:
        public double x; 

        public double y;



        // Método ToString para impressão:
        public override string ToString()
        {
            return "(" + x + "," + y + ")";
        }
    
    }


}
